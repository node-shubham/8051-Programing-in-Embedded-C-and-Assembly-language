; PROG TO SEND "YES" CONTINUOUSLY AT BAUD RATE 4800
CSEG AT 0
	MOV TMOD, #0X20 	; T_1 MODE 2 : 8 BIT AUTO RELAOD
	MOV TH1, #-6 		; #0XFDh | BAUD RATE : 4800
	MOV SCON, #0X50 	; #0101 0000b | SERIAL MODE 1
	SETB TR1 			; RUN TIMER 1
REP:MOV A, #"Y" 
	ACALL SEND
	MOV A, #"E" 
	ACALL SEND
	MOV A, #"S" 
	ACALL SEND
	SJMP REP			; SEND CONTINUOUSLY
	SEND: MOV SBUF, A	; READY TO TRANSMIT
	WAIT: JNB TI, WAIT	; WAIT UNTIL TRANSFER IS COMPLETE
	CLR TI				; CLEAR TI FLAG
	RET			
END	
	
