; PROG TO SEND 'A' CONTINUOUSLY AT BAUD RATE 9600
CSEG AT 0
	MOV TMOD, #0X20 	; T_1 MODE 2 : 8 BIT AUTO RELAOD
	MOV TH1, #-3 		; #0XFDh | BAUD RATE : 9600
	MOV SCON, #0X50 	; #0101 0000b | SERIAL MODE 1
	SETB TR1 			; RUN TIMER 1
	
	L1: JNB RI, L1		; WAIT UNTIL RECEIVING IS COMPLETE
	MOV A, SBUF			; DATA STORED
	MOV P2, A			; DATA SEND TO P2
	MOV SBUF, A  		; DATA READY TO SEND 
	WAIT: JNB TI, WAIT
	CLR RI				; READY TO RECEIVE NEXT BYTE
	CLR TI
	SJMP L1				; CONTINUOUS ACTION
END	
	
